// 공유 카드용 한국 미니 지도 생성기.
// provinces 토포json → 전국 외곽선(내부 시도 경계 제거) + 오행별 대표 지역 핀 좌표.
// 140px 미니맵용이라 픽셀 공간에서 점을 과감히 솎아내(decimate) 경량 path를 굽는다.
// 결과: lib/koreaMini.ts (인라인 SVG라 html2canvas 캡처에 안전).
//   node scripts/gen-korea-mini.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { feature, merge } from "topojson-client";
import { geoMercator } from "d3-geo";

const W = 200;
const H = 248;
const MIN_GAP = 1.6; // 픽셀: 이보다 가까운 연속 점은 제거
const MIN_RING_PTS = 5; // 단순화 후 이보다 적은 점의 섬은 버림

const topo = JSON.parse(readFileSync("public/maps/provinces.json", "utf8"));
const objKey = Object.keys(topo.objects)[0];
const geometries = topo.objects[objKey].geometries;
const collection = feature(topo, topo.objects[objKey]);

// 투영은 전체로 맞추고, 외곽은 내부 경계가 제거된 merge 결과 사용.
const projection = geoMercator().fitSize([W, H], collection);
const merged = merge(topo, geometries); // MultiPolygon (lng/lat), 내부 시도선 제거

const round = (n) => Math.round(n * 10) / 10;

function simplifyRing(ring) {
  const out = [];
  let last = null;
  for (const lnglat of ring) {
    const p = projection(lnglat);
    if (!p) continue;
    if (last && Math.hypot(p[0] - last[0], p[1] - last[1]) < MIN_GAP) continue;
    out.push([round(p[0]), round(p[1])]);
    last = p;
  }
  return out;
}

const subPaths = [];
for (const polygon of merged.coordinates) {
  for (const ring of polygon) {
    const simplified = simplifyRing(ring);
    if (simplified.length < MIN_RING_PTS) continue;
    subPaths.push("M" + simplified.map(([x, y]) => `${x},${y}`).join("L") + "Z");
  }
}
const outline = subPaths.join("");

// 오행별 대표 지역 경위도(OBANG_REGIONS 첫 지역 기준). [경도, 위도]
const ANCHOR_LNGLAT = {
  목: [128.47, 38.19], // 설악산·속초 (동/산)
  화: [126.53, 33.49], // 제주 (남/화산)
  토: [128.52, 36.54], // 안동 하회마을 (중앙/내륙)
  금: [126.98, 37.57], // 서울 도심 (서/도시)
  수: [128.90, 37.80], // 강릉·동해안 (북/바다)
};
const anchors = {};
for (const [el, lnglat] of Object.entries(ANCHOR_LNGLAT)) {
  const [x, y] = projection(lnglat);
  anchors[el] = { x: round(x), y: round(y) };
}

const out = `// AUTO-GENERATED by scripts/gen-korea-mini.mjs — do not edit by hand.
// 공유 카드용 한국 미니 지도(고정 viewBox ${W}x${H}, 단순화) + 오행별 대표 지역 핀 좌표.
export const KOREA_MINI_VIEW = { w: ${W}, h: ${H} };
export const KOREA_MINI_PATH = ${JSON.stringify(outline)};
export const KOREA_MINI_ANCHORS: Record<string, { x: number; y: number }> = ${JSON.stringify(anchors, null, 2)};
`;
writeFileSync("lib/koreaMini.ts", out);
console.log("wrote lib/koreaMini.ts", { bytes: out.length, rings: subPaths.length, anchors });
